"""
This module defines a Position class for representing a position in 2D space.
"""
class Position:
    """
    This is a class for representing a position in 2D space.
    """
    def __init__(self, x: float, y: float):
        """
        Initializes a new instance of the Position class.
        
        Args:
            x (float): The x-coordinate of the position.
            y (float): The y-coordinate of the position.
        """
        self.x = x
        self.y = y
